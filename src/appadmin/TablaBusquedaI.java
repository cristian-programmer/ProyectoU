    private void initTable(String direccion) {
        model = (DefaultTableModel) this.jTable1.getModel();

        Inmueble[] inmuebles = Inmueble.loadInmuebles();
        for (Inmueble inmueble : inmuebles) {
            if (direccion.equals(inmueble.getDireccion())) {
                model.addRow(new Object[]{
                inmueble.getEstado(),
                inmueble.getDocumento(),
                inmueble.getDireccion(),
                inmueble.getBarrio(),
                inmueble.getTelefono(),
                inmueble.getnAlcobas(),
                inmueble.getAreaTotal()
            ,"$"+inmueble.getPrecio()});
            }
        }
    }

